/// Consistent spacing system for the app
class AppSpacing {
  // Base spacing unit (4px)
  static const double unit = 4.0;

  // Spacing scale
  static const double xs = unit; // 4px
  static const double sm = unit * 2; // 8px
  static const double md = unit * 3; // 12px
  static const double lg = unit * 4; // 16px
  static const double xl = unit * 5; // 20px
  static const double xxl = unit * 6; // 24px
  static const double xxxl = unit * 8; // 32px
  static const double huge = unit * 10; // 40px
  static const double massive = unit * 12; // 48px

  // Common padding values
  static const double paddingXS = xs;
  static const double paddingSM = sm;
  static const double paddingMD = md;
  static const double paddingLG = lg;
  static const double paddingXL = xl;
  static const double paddingXXL = xxl;

  // Common margin values
  static const double marginXS = xs;
  static const double marginSM = sm;
  static const double marginMD = md;
  static const double marginLG = lg;
  static const double marginXL = xl;
  static const double marginXXL = xxl;

  // Border radius values
  static const double radiusXS = xs;
  static const double radiusSM = sm;
  static const double radiusMD = md;
  static const double radiusLG = lg;
  static const double radiusXL = xl;
  static const double radiusXXL = xxl;
  static const double radiusRound = 999; // For circular elements

  // Icon sizes
  static const double iconXS = lg; // 16px
  static const double iconSM = xl; // 20px
  static const double iconMD = xxl; // 24px
  static const double iconLG = unit * 7; // 28px
  static const double iconXL = xxxl; // 32px
  static const double iconXXL = huge; // 40px

  // Avatar sizes
  static const double avatarSM = xxxl; // 32px
  static const double avatarMD = huge; // 40px
  static const double avatarLG = massive; // 48px
  static const double avatarXL = unit * 14; // 56px
  static const double avatarXXL = unit * 16; // 64px

  // Button heights
  static const double buttonSM = xxxl; // 32px
  static const double buttonMD = huge; // 40px
  static const double buttonLG = massive; // 48px
  static const double buttonXL = unit * 14; // 56px

  // Input field heights
  static const double inputSM = xxxl; // 32px
  static const double inputMD = huge; // 40px
  static const double inputLG = massive; // 48px

  // Card padding
  static const double cardPaddingSM = md; // 12px
  static const double cardPaddingMD = lg; // 16px
  static const double cardPaddingLG = xxl; // 24px

  // Screen padding
  static const double screenPaddingH = lg; // 16px horizontal
  static const double screenPaddingV = xxl; // 24px vertical
}
